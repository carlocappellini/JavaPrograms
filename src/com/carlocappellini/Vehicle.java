package com.carlocappellini;

public class Vehicle {

    private int wheels;
    private int doors;
    private int speed;
    private int gears;
    private String color;



    public Vehicle(int wheels, int doors, int speed, int gears, String color){
        this.wheels = wheels;
        this.doors = doors;
        this.speed = speed;
        this.gears = gears;
        this.color = color;

    }

}

